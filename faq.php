<div class="faqpergunta">
    <h2>Por que o Linux não pega vírus?</h2>
</div>
<br>
<div class="faqresposta">
    <p>O Linux é um sistema operacional diferente do Windows. O Linux não pega vírus porque os vírus são criados para Windows, geralmente em arquivos “.exe”, “.com” e “.bat”, que são arquivos que não são executados no Linux.
Ou seja, o Linux não só pega vírus, quanto não executa os executáveis do Windows.</p>
</div>
<div class="faqpergunta">
    <h2>É necessário ter um antivírus instalado no computador?</h2>
</div>
<br>
<div class="faqresposta">
    <p> Sim. Um Software Antivírus é necessário para proteger o seu computador contra ameaças virtuais, como vírus, trojans, etc. Um computador sem antivírus é um alvo mais fácil para essas ameaças da internet. Mas só ter um antivírus não resolve; você deve saber Como não Pegar Vírus e Como Navegar com Mais Segurança.</p>
</div>
